# operadores aritméticos
x = 50
y = 2

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x // y)
print(x ** y)
print(y ** x)
print(x % y)
print()

# operadores lógicos

tem_cafe = True
tem_pao = False

print(not tem_cafe)
print(tem_cafe or tem_pao)
print(tem_cafe and tem_pao)
print()

# operadores relacionais

dolar = 5.3
real = 1.0

print(dolar > real)
print(dolar < real)
print(dolar == real)
print(dolar >= real)
print(dolar <= real)
print(dolar != real)
print()










